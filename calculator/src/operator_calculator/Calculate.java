package operator_calculator;

public class Calculate {
	
	public int plus(int num1, int num2) {
		return num1+num2;
	}
	
	public int minus(int num1, int num2) {
		return num1-num2;
	}
	
	public int product(int num1, int num2) {
		return num1*num2;
	}
	
	public int devide(int num1, int num2) {
		return num1/num2;
	}

}
